let DB_CONNECTED = false;
async function connectMongo() {
  if (!MONGO_URI) {
    console.warn("‚ö†Ô∏è Skipping MongoDB connection because MONGODB_URI is not set.");
    DB_CONNECTED = false;
    return;
  }

  try {
    console.log(`üì° Connecting to MongoDB at ${new Date().toISOString()}...`);
    await mongoose.connect(MONGO_URI);
    DB_CONNECTED = true;
    console.log("‚úÖ MongoDB connected successfully!");
  } catch (err) {
    DB_CONNECTED = false;
    console.warn(`‚ö†Ô∏è MongoDB connection failed at ${new Date().toISOString()}: ${err.message}`);
    console.warn("Starting server in degraded mode. To enable database features, start a MongoDB instance or set MONGODB_URI in your environment.");
  }
}

