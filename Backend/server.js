async function startAgenda() {
  if (!DB_CONNECTED) {
    console.warn("‚ö†Ô∏è Agenda will not start because MongoDB is not connected.");
    return;
  }

  try {
    agenda = new Agenda({
      db: { address: MONGO_URI, collection: "agendaJobs" },
    });

    agenda.define("test job", async () => {
      console.log(`‚è≥ Running test job at ${new Date().toISOString()}`);
    });

    await agenda.start();
    await agenda.every("1 minute", "test job");

    console.log("üìÖ Agenda job scheduler started!");
  } catch (err) {
    console.error(`‚ùå Agenda startup error at ${new Date().toISOString()}:`, err);
  }
}

